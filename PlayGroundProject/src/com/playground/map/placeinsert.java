package com.playground.map;

import java.util.ArrayList;

public class placeinsert {

	
	private String names;
	private ArrayList<Object> types;
	private String vicinitys;
	private String photo_references;
	private Object ratings;
	private double lats;
	private double lngs;
	private String html_attributions;
	
	public placeinsert() {}
	
	
	public String getNames() {
		return names;
	}
	public void setNames(String names) {
		this.names = names;
	}
	public ArrayList<Object> getTypes() {
		return types;
	}
	public void setTypes(ArrayList<Object> types) {
		this.types = types;
	}
	public String getVicinitys() {
		return vicinitys;
	}
	public void setVicinitys(String vicinitys) {
		this.vicinitys = vicinitys;
	}
	public String getPhoto_references() {
		return photo_references;
	}
	public void setPhoto_references(String photo_references) {
		this.photo_references = photo_references;
	}
	public Object getRatings() {
		return ratings;
	}
	public void setRatings(Object ratings) {
		this.ratings = ratings;
	}
	public double getLats() {
		return lats;
	}
	public void setLats(double lats) {
		this.lats = lats;
	}
	public double getLngs() {
		return lngs;
	}
	public void setLngs(double lngs) {
		this.lngs = lngs;
	}


	public String getHtml_attributions() {
		return html_attributions;
	}


	public void setHtml_attributions(String html_attributions) {
		this.html_attributions = html_attributions;
	}
	
	
	
	
}
