package com.playground.vo;

public class FestivalCount {

	private int aCode;
	private int count;
	
	public FestivalCount() {}
	
	
	
	public FestivalCount(int aCode, int count) {
		this.aCode = aCode;
		this.count = count;
	}



	public int getaCode() {
		return aCode;
	}
	public void setaCode(int aCode) {
		this.aCode = aCode;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	
}
